const arabicTextToKey = new Map<string, string>();

Object.entries(translations.ar).forEach(([key, value]) => {
  if (value) arabicTextToKey.set(value, key);
});

Object.entries(explicitLegacyTextKeys).forEach(([value, key]) => {
  arabicTextToKey.set(value, key);
});

const replaceableLegacyEntries = Array.from(arabicTextToKey.entries()).sort((a, b) => b[0].length - a[0].length);

function preserveWhitespace(original: string, replacement: string): string {
  const leading = original.match(/^\s*/)?.[0] || '';
  const trailing = original.match(/\s*$/)?.[0] || '';
  return `${leading}${replacement}${trailing}`;
}

export function translateLegacyText(text: string, language: Language = getStoredLanguage()): string {
  if (!text.trim()) return text;

  const trimmed = text.trim();
  const exactKey = arabicTextToKey.get(trimmed);
  if (exactKey) {
    return preserveWhitespace(text, translateText(exactKey, language));
  }

  let translated = text;
  for (const [source, key] of replaceableLegacyEntries) {
    if (!source || translated === source || !translated.includes(source)) continue;
    translated = translated.split(source).join(translateText(key, language));
  }

  return translated;
}
