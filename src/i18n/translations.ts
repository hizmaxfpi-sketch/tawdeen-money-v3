export function getLanguageDirection(language: Language): 'rtl' | 'ltr' {
  return language === 'ar' ? 'rtl' : 'ltr';
}

export function getStoredLanguage(): Language {
  if (typeof window === 'undefined') return 'ar';
  const saved = localStorage.getItem('tawdeen-language');
  return saved === 'en' || saved === 'tr' || saved === 'ar' ? saved : 'ar';
}

function interpolateTranslation(template: string, params?: TranslationParams): string {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, key) => String(params[key] ?? `{${key}}`));
}

export function translateText(key: string, language: Language = getStoredLanguage(), params?: TranslationParams): string {
  const template = translations[language]?.[key] || translations.ar[key] || key;
  return interpolateTranslation(template, params);
}

